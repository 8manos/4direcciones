sta empresa. <strong>DOCE</strong> Agradecemos ser herederos de fortuna del sol y la luna que nos dejaron nuestros abuelos de las cuatro direcciones. <strong>TRECE</strong> Agradecemos a los Osos de Anteojos por existir y pedimos que sigan danzando por la continuidad de la vida.</p>
